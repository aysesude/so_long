#include "ft_printf.h"

int	ft_print_nbr(int nbr)
{
	int	i;

	i = 0;
	if (nbr == 0)
	{
		ft_putchar('0');
		return (1);
	}
	i += ft_put_nbr(nbr);
	return (i);
}
